'''
Escribe un programa que pregunte al usuario dos números y calcule su suma, resta, multiplicación, división, módulo y potencia
'''

a = int(input("Dame un número: "))
b = int(input("Dame otro número: "))

print()

print("Suma:", a + b)
print("Resta:", a - b)
print("Multiplicación:", a * b)
print("División (no entera):", a / b)
print("Módulo:", a % b)
print("Potencia:", a ** b)